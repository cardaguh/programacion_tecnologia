                    <p>Si quieres aprender a programar es algo que puedes hacer de inmediato, existen muchos recursos para poder aprender tales como libros, cursos online y presenciales, en este caso te presento algunos buenos recursos por donde empezar esta ardua pero divertida labor de programar.</p>



                    <h2 class="section-heading">Codeacademy</h2>

                    <a href="https://www.codecademy.com/" target="_blank">
                        <img class="img-responsive" src="img/codecademy.jpg" alt="">
                    </a>

                    <p>En mi opinión este es posiblemente el mejor recurso para aprender a programar online, ya que te permite practicar todo lo aprendido desde una consola embebida en la plataforma, y tiene cursos tan básicos e importantes como HTML, CSS, JavaScript, SQL y sobre frameworks tan usados actualmente como Ruby On Rails, la verdad muy recomendada esta opción. Link: <a href="https://www.codecademy.com/" target="_blank"> Codeacademy</a></p>


                    <h2 class="section-heading">Coursera</h2>

                    <a href="https://www.coursera.org/" target="_blank">
                        <img class="img-responsive" src="img/coursera.png" alt="">
                    </a>

                    <p> Esta es una plataforma de aprendizaje online, en esta no solo hay cursos sobre programación, sino además de matemáticas, estadística, negocios y demás ciencias, todos estos cursos impartidos por las mejores universidades del mundo, el único defecto es que para obtener un certificado hay que pagar por este (Inicialmente no era así). Link: <a href="https://www.coursera.org/" target="_blank"> Coursera</a></p>

                    <h2 class="section-heading">edX</h2>

                    <a href="https://www.edx.org/" target="_blank">
