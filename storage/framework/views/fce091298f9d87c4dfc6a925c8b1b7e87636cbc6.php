                    <p>Te mostraré varios recursos muy útiles si quieres comenzar a desarrollar aplicaciones para la plataforma Android, miraremos primero las opciones nativas, sin dejar de un lado el desarrollo de aplicaciones móviles híbridas con Frameworks como Ionico o Phonegap.</p>


                    <h2 class="section-heading">Android Studio</h2>

                    <img class="img-responsive" src="img/android_studio.png" alt="">

                    <p>Para comenzar a desarrollar aplicaciones con Android el primer paso es instalar Android Studio desde este link: <a href="https://developer.android.com/studio/index.html" target="_blank">Descargar Android Studio</a></p>

                    <p>Ya que la plataforma de aplicaciones de Android se basa en Java debemos instalar el JDK(Kit de desarrollo de Java) de Java desde aquí: <a href="http://www.oracle.com/technetwork/java/javase/downloads/jdk8-downloads-2133151.html" target="_blank">JDK de Java</a></p>

                    <p>Luego de haber instalado las APIs de Android y el entorno de desarrollo ya podemos conectar nuestro dispositivo Android al computador y comenzar a desarrollar aplicaciones!</p>

                    <p>Te dejo el link de un tutorial muy bueno sobre android: <a href="https://www.youtube.com/watch?v=QAbQgLGKd3Y&list=PL6gx4Cwl9DGBsvRxJJOzG4r4k_zLKrnxl" target="_blank">Video tutorial de Android</a></p>

                    <p>Además de esto te sugiero que revises la documentación oficial y la API de Android cuando tengas alguna duda sobre alguna clase o algún tema referente a android: <a href="https://developer.android.com/index.html" target="_blank">Documentación oficial de Android</a></p>

